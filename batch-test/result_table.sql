-- 업종별 카드 사용액
CREATE TABLE IF NOT EXISTS STATISTICS_BY_BUSINESS_TYPE(
    BUSINESS_TYPE VARCHAR(255),
    TOTAL_AMOUNT INT
) COMMENT 'Statistics per Business Type';

-- 성별 카드 사용액
CREATE TABLE IF NOT EXISTS STATISTICS_BY_SEX(
    SEX VARCHAR(255),
    TOTAL_AMOUNT INT
) COMMENT 'Statistics per User Sex';

-- 연령별 카드 사용액
CREATE TABLE IF NOT EXISTS STATISTICS_BY_AGE(
    AGE INT,
    TOTAL_AMOUNT INT
) COMMENT 'Statistics per Age';
